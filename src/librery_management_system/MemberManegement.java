      private void genareteMemberID() {
        try {
            ResultSet rs = DB.serch("select count(mem_id) as x from member");
            if(rs.next()){
                String counts = rs.getString("x");
                System.out.println("aa");
                int count = Integer.parseInt(counts);
               ++count;
                if(count<10){
                txtReId.setText("LM000"+count);
                txtReId.setEditable(false);
                }else if(count<100){
                 txtReId.setText("LM00"+count);
                txtReId.setEditable(false);
                }else if(count<1000){
                    txtReId.setText("LM0"+count);
                txtReId.setEditable(false);
                }else if(count<10000){
                    txtReId.setText("LM"+count);
                txtReId.setEditable(false);
                }
            }
            txtFname.grabFocus();
        } catch (Exception e) {
            System.out.println(e);
        }
    }

